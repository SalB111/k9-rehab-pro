// Storyboard coverage verification script — checks live API
const http = require('http');

http.get('http://localhost:3000/api/exercises', (res) => {
  const chunks = [];
  res.on('data', c => chunks.push(c));
  res.on('end', () => {
    const exercises = JSON.parse(Buffer.concat(chunks).toString());
    console.log('Total exercises from API:', exercises.length);
    console.log('');

    let withStoryboard = 0;
    let withoutStoryboard = 0;
    let autoGenerated = 0;
    let handAuthored = 0;
    const missing = [];
    const byCat = {};

    exercises.forEach(ex => {
      const cat = ex.category || 'Unknown';
      if (!byCat[cat]) byCat[cat] = { total: 0, ok: 0, auto: 0, manual: 0, missing: [] };
      byCat[cat].total++;

      const ce = ex.client_education || {};
      if (ce.storyboard_available) {
        withStoryboard++;
        byCat[cat].ok++;
        if (ce.storyboard_metadata && ce.storyboard_metadata.auto_generated) {
          autoGenerated++;
          byCat[cat].auto++;
        } else {
          handAuthored++;
          byCat[cat].manual++;
        }
      } else {
        withoutStoryboard++;
        missing.push(ex.code + ': ' + ex.name + ' (' + cat + ')');
        byCat[cat].missing.push(ex.code);
      }
    });

    console.log('='.repeat(60));
    console.log('  STORYBOARD COVERAGE AUDIT');
    console.log('='.repeat(60));
    console.log('');

    const cats = Object.keys(byCat).sort();
    cats.forEach(cat => {
      const c = byCat[cat];
      const status = c.ok === c.total ? 'ALL COVERED' : (c.total - c.ok) + ' MISSING';
      const detail = c.manual > 0 ? c.manual + ' hand-authored, ' + c.auto + ' auto-generated' : c.auto + ' auto-generated';
      console.log('  ' + cat + ' (' + c.ok + '/' + c.total + '): ' + status + ' [' + detail + ']');
      c.missing.forEach(code => console.log('      MISSING: ' + code));
    });

    console.log('');
    console.log('='.repeat(60));
    console.log('  SUMMARY');
    console.log('='.repeat(60));
    console.log('  Total exercises:          ' + exercises.length);
    console.log('  With storyboard:          ' + withStoryboard + '/' + exercises.length);
    console.log('    Hand-authored:          ' + handAuthored);
    console.log('    Auto-generated:         ' + autoGenerated);
    console.log('  Missing storyboard:       ' + withoutStoryboard);
    console.log('');

    if (missing.length > 0) {
      console.log('  EXERCISES WITHOUT STORYBOARD:');
      missing.forEach(m => console.log('    ' + m));
    } else {
      console.log('  ALL ' + exercises.length + ' EXERCISES HAVE STORYBOARD COVERAGE');
    }
  });
}).on('error', err => {
  console.log('ERROR: Could not reach API. Is the backend running?', err.message);
});
